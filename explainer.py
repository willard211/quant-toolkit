def print_explanations():
    """
    打印主要量化指标的“白话文”通俗解释，帮助非金融专业人士理解输出结果。
    由于命令行编码限制，尽量使用纯文本，不使用复杂的 Emoji。
    """
    print("=" * 60)
    print("  QUANTITATIVE METRICS EXPLAINER (小白指标翻译机)  ")
    print("=" * 60)
    
    print("\n[1] Ann. Return (%) - 年化收益率")
    print("    * 官方释义：资产在一年内的等效复利增长率。")
    print("    * 大白话：如果你这笔钱每年都按这个速度生娃，一年能生出百分之多少。")
    print("    *  怎么看：越大越好，美股大盘常年在 10% 左右。")

    print("\n[2] Ann. Volatility (%) - 年化波动率")
    print("    * 官方释义：资产收益率标准差的年化值，衡量价格非预期波动的剧烈程度。")
    print("    * 大白话：这只股票脾气爽不爽？上蹿下跳有多严重。")
    print("    *  怎么看：大盘在 15% 左右是“乖宝宝”；超过 40% 的都是“过山车”，比如特斯拉。波动率越大，期权费越贵。")

    print("\n[3] Sharpe (rf=5%) - 夏普比率 (扣除5%无风险存款后)")
    print("    * 官方释义：每承受 1 单位总风险，能产生多少超额回报。")
    print("    * 大白话：性价比！你担惊受怕一年，换来的钱比直接存定期多多少？")
    print("    *  怎么看：")
    print("      < 0.5  = 性价比极低，瞎折腾。")
    print("      0.5~1.0= 一般般，跟买大盘差不多。")
    print("      > 1.0  = 极品！承受同样的惊吓，它能给你远超常人的利润。")

    print("\n[4] Variance Drag - 方差拖曳 (损耗)")
    print("    * 官方释义：资产收益率因为非对称涨跌（如先跌50%需涨100%才能回本）造成的长期复利损耗。")
    print("    * 大白话：哪怕这只股票平均每天涨 1%，跌 1%，长期拿在手里你的账户也是在疯狂滴血的。")
    print("    *  怎么看：股票波动越大，即使不跌，在横盘震荡里把你耗尽的钱就越多。所以少碰那种大涨大跌的妖股。")

    print("\n[5] Max Drawdown - 最大回撤")
    print("    * 官方释义：在选定周期内，产品净值走到最低点时的收益率回撤幅度的最大值。")
    print("    * 大白话：如果世界上最倒霉的人，买在了历史最高点，卖在了历史最低谷，他会亏多少钱？")
    print("    *  怎么看：你的“心脏病发作指数”。如果你受不了账户资产蒸发 30%，就绝对不要碰最大回撤超过 30% 的资产，因为它历史上真的发生过。")

    print("\n" + "=" * 60 + "\n")
